static void g_N(const u8* N, u8* h, const u8* m)
{
	u8 t[64], K[64];

	AddXor512(N, h, K);
	F(K);
	E(K, m, t);
	AddXor512(t, h, t);
	AddXor512(t, m, h);
}

void Hasher::hash(const byte* src, byte* hash, size_t srcLength)
{
	u8 v512[64] = { 0 };
	u8 v0[64] = { 0 };
	u8 Sigma[64] = { 0 };
	u8 N[64] = { 0 };
	u8 m[64];
	u64 len = srcLength * 8;

	memset(hash, 0, 64);
	v512[62] = 0x02;

	// Stage 2
	while (len >= 512)
	{
		memcpy(m, src + len / 8 - 63 - ((len & 0x7) == 0), 64);

		g_N(N, hash, m);
		AddModulo512(N, v512, N);
		AddModulo512(Sigma, m, Sigma);
		len -= 512;
	}

	memset(m, 0, 64);
	memcpy(m + 63 - len / 8 + ((len & 0x7) == 0), src, (uint)(len / 8 + 1 - ((len & 0x7) == 0)));

	// Stage 3
	m[63 - len / 8] |= (1 << (len & 0x7));

	g_N(N, hash, m);
	v512[63] = len & 0xFF;
	v512[62] = (u8)(len >> 8);
	AddModulo512(N, v512, N);

	AddModulo512(Sigma, m, Sigma);

	g_N(v0, hash, N);
	g_N(v0, hash, Sigma);
}

